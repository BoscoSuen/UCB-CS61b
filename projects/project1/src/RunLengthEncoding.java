  /**
   *  check() walks through the run-length encoding and prints an error message
   *  if two consecutive runs have the same RGB intensities, or if the sum of
   *  all run lengths does not equal the number of pixels in the image.
   */
  public void check() {
    // Your solution here.
	  DListNode current = this.RList.getHead();
	  int runlength = current.item[0];
	  boolean notEqual = false;
	  while (current != null & current.next != null) {		// to the second-last node.
		  if (current.item[1] == current.next.item[1] && current.item[2] == current.next.item[2] && current.item[3] == current.next.item[3]) {
			  notEqual = true;
			  break;
		  }
		  current = current.next;			// can get to the last node.
		  runlength = runlength + current.item[0];		  
	  }
	  if (notEqual) {
		  System.out.println("Error: two consecutive runs have the same RGB intensities!");
		  System.exit(0);
	  }
	  if (runlength != width*height) {
		  System.out.println("Error: all run lengths does not equal the number of pixels in the image!");
		  System.exit(0);
	  }
  }


